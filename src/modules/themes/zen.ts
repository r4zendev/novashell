type ZenProfile = {
	path: string;
};

function parseProfilesIni(contents: string, zenRoot: string): ZenProfile[] {
	const profiles: ZenProfile[] = [];
	const seen = new Set<string>();

	let section = "";
	let profilePath = "";
	let isRelative = true;

	const pushProfile = () => {
		if (!section.startsWith("Profile") || !profilePath) return;

		const resolvedPath = isRelative ? `${zenRoot}/${profilePath}` : profilePath;
		if (!GLib.file_test(resolvedPath, GLib.FileTest.IS_DIR)) return;
		if (seen.has(resolvedPath)) return;

		seen.add(resolvedPath);
		profiles.push({ path: resolvedPath });
	};

	for (const rawLine of contents.split("\n")) {
		const line = rawLine.trim();
		if (!line || line.startsWith("#") || line.startsWith(";")) continue;

		if (line.startsWith("[") && line.endsWith("]")) {
			pushProfile();
			section = line.slice(1, -1);
			profilePath = "";
			isRelative = true;
			continue;
		}

		const eqIndex = line.indexOf("=");
		if (eqIndex < 1) continue;

		const key = line.slice(0, eqIndex).trim().toLowerCase();
		const value = line.slice(eqIndex + 1).trim();

		if (key === "path") profilePath = value;
		if (key === "isrelative") {
			isRelative = value === "1" || value.toLowerCase() === "true";
		}
	}

	pushProfile();

	return profiles;
}

function getZenProfiles(): ZenProfile[] {
	const zenRoot = `${GLib.get_home_dir()}/.zen`;
	const profilesIni = `${zenRoot}/profiles.ini`;

	if (!GLib.file_test(profilesIni, GLib.FileTest.EXISTS)) return [];

	try {
		const content = readFile(profilesIni);
		return parseProfilesIni(content, zenRoot);
	} catch (e) {
		console.error(`ColorUtils: Failed to read Zen profiles.ini: ${e}`);
		return [];
	}
}

