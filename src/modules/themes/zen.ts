async function ensureLegacyStylesheetPref(profilePath: string): Promise<void> {
	const prefLine =
		'user_pref("toolkit.legacyUserProfileCustomizations.stylesheets", true);';
	const userJsPath = `${profilePath}/user.js`;

	let contents = "";
	try {
		if (GLib.file_test(userJsPath, GLib.FileTest.EXISTS)) {
			contents = readFile(userJsPath);
		}
	} catch (e) {
		console.error(`ColorUtils: Failed to read ${userJsPath}: ${e}`);
	}

	if (
		contents.includes("toolkit.legacyUserProfileCustomizations.stylesheets")
	) {
		return;
	}

	const next = `${contents.trim()}\n${prefLine}\n`;
	await writeFileAsync(userJsPath, next).catch((e: Error) => {
		console.error(`ColorUtils: Failed to write Zen user.js: ${e.message}`);
	});
}

export async function updateZenTheme(
	data: ColorData,
	p: DerivedPalette,
): Promise<void> {
	const profiles = getZenProfiles();
	if (profiles.length === 0) return;

	const userChrome = buildUserChrome(data, p);
	const userContent = buildUserContent(data, p);
	const logoSvg = buildLogoSvg(p);
	const iconThemeDir = `${GLib.get_home_dir()}/.local/share/icons/hicolor/scalable/apps`;
	const zenIconPath = `${iconThemeDir}/zen-logo-novashell.svg`;

	ensureDirectory(iconThemeDir);
	await writeFileAsync(zenIconPath, logoSvg).catch((e: Error) => {
		console.error(
			`ColorUtils: Failed to write Novashell Zen icon (${zenIconPath}): ${e.message}`,
		);
	});

	await Promise.all(
		profiles.map(async (profile) => {
			const chromeDir = `${profile.path}/chrome`;
			ensureDirectory(chromeDir);

			await Promise.all([
				writeFileAsync(`${chromeDir}/userChrome.css`, userChrome).catch(
					(e: Error) => {
						console.error(
							`ColorUtils: Failed to write Zen userChrome.css (${profile.path}): ${e.message}`,
						);
					},
				),
				writeFileAsync(`${chromeDir}/userContent.css`, userContent).catch(
					(e: Error) => {
						console.error(
							`ColorUtils: Failed to write Zen userContent.css (${profile.path}): ${e.message}`,
						);
					},
				),
				writeFileAsync(`${chromeDir}/zen-logo-novashell.svg`, logoSvg).catch(
					(e: Error) => {
						console.error(
							`ColorUtils: Failed to write Zen themed logo (${profile.path}): ${e.message}`,
						);
					},
				),
				ensureLegacyStylesheetPref(profile.path),
			]);
		}),
	);
}
